public class Fifteen {
    static int mul(int a){
        int num=0;
        for(int i=1;i<=10;i++){
            num=a*i;
            System.out.println(a +" * "+i+" = "+num);
        }
            return 0;
        }

        public static void main(String[] args){
            int a=2;
            mul(a);
        }
    }
    
